package com.istef.southpark.exception;

public class MgidParserException extends ParserException {
	private static final long serialVersionUID = -1587735711229705427L;

	public MgidParserException() {
		super();
	}

	public MgidParserException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public MgidParserException(String message, Throwable cause) {
		super(message, cause);
	}

	public MgidParserException(String message) {
		super(message);
	}

	public MgidParserException(Throwable cause) {
		super(cause);
	}

}
