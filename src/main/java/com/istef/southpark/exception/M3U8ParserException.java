package com.istef.southpark.exception;

public class M3U8ParserException extends ParserException {
	private static final long serialVersionUID = 6396018089540668664L;

	public M3U8ParserException() {
		super();
	}

	public M3U8ParserException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public M3U8ParserException(String message, Throwable cause) {
		super(message, cause);
	}

	public M3U8ParserException(String message) {
		super(message);
	}

	public M3U8ParserException(Throwable cause) {
		super(cause);
	}

}
